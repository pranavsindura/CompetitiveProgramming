void cp()
{
    int n;
    ll k;
    cin >> n >> k;
    vector<ll> arr(n);
    for(ll &x : arr)
        cin >> x;
    bool ok = true;
    map<int, int> p;
    for(ll x : arr)
    {
        int exp = 0;
        while(x)
        {
            int rem = x % k;
            if(rem > 1) ok = false;
            if(rem == 1) p[exp]++;
            exp++;
            x /= k;
        }
    }
    for(auto exp : p)
        if(exp.ss > 1)
            ok = false;
    cout << (ok ? "YES\n" : "NO\n");
}

