/*
	We deal 6 random damage and then 3 damage in one go
	every 7 moves we deal a total of 9 damage
	total power must be divisible by 9
	and total / 9 is the number of groups of 7 moves we perform
	they must be <= the minimum power, otherwise that min power will not survive the total sessions
	it will die early
*/

void cp()
{
    int a, b, c;
    cin >> a >> b >> c;
    bool ok = (a + b + c) % 9 == 0 && min({a, b, c}) >= (a + b + c) / 9;
    cout << (ok ? "YES\n" : "NO\n");
}

