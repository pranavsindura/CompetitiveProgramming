void cp()
{
    ll n, m;
    cin >> n >> m;
    ll ans = n * (n + 1) / 2;
    ll z = n - m;
    ll g = z / (m + 1);
    ll r = z % (m + 1);
    ans -= (m + 1 - r) * g * (g + 1) / 2;
    ans -= r * (g + 1) * (g + 2) / 2;
    cout << ans << endl;
}

