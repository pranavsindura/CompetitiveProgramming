void cp()
{
    string s;
    cin >> s;
    ll even = 0, odd = 0;
    vector<vector<int>> cnt(2, vector<int>(2, 0));
    for(char x : s)
    {
        cnt[x - 'a'][0]++;
        odd += cnt[x - 'a'][0];
        even += cnt[x - 'a'][1];

        swap(cnt[0][0], cnt[0][1]);
        swap(cnt[1][0], cnt[1][1]);
    }

    cout << even << " " << odd << endl;
}

